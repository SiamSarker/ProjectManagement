
public class DataType {

    public static void main(String[] argc) {

        boolean b = true;
        char c = 'a';
        short s = 32677;
        int i = 126587;
        float f = 10.2f;
        double d = 10.2;

        System.out.printf("b = %b", b);

        System.out.printf("\nc = %c", c);

        System.out.printf("\ns = %d", s);

        System.out.printf("\ni = %d", i);

        System.out.printf("\nf = %.2f", f);

        System.out.printf("\nd = %f\n", d);

    }
}