/**
 * WellFormed
 */
public class WellFormed {

    public static void main(String[] args) {
        System.out.print("A well-formed Java program has\na main method with { and }\nbraces.\n");
        System.out.println();
        System.out.print(
                "A System.out.println statement\nhas ( and ) and usually a\nString that starts and ends\nwith a \" character.\n");
        System.out.println("(But we type \\\" instead!)\n");
        System.out.println("hello world");
    }
}